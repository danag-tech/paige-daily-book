def build_summary_prompt(books: list) -> str:
    book_sections = []
    for index, book in enumerate(books, start=1):
        book_sections.append(
            "\n".join(
                [
                    f"{index}. 书名：{book.title}",
                    f"作者：{book.author}",
                    f"ISBN：{book.isbn or '暂无'}",
                    f"评分：{book.rating or '暂无'}",
                    f"简介：{book.summary or '暂无'}",
                ]
            )
        )

    books_text = "\n\n".join(book_sections)

    return f"""请根据以下三本书的信息，生成一份中文每日荐书内容。

可使用的书籍字段只有：书名、作者、ISBN、评分、简介。
书籍简介长度：每本书正文必须保持 150-250 个中文汉字；少于 150 个汉字需要补充必要事实信息，超过 250 个汉字视为失败，生成后必须自行检查并压缩。
不要编造书籍信息；如果 ISBN、评分或简介缺失，要如实写“暂无”。
不要输出 Markdown 表格，不要输出 HTML，不要新增封面、邮件或其他数据。
必须只输出一个合法 JSON 对象，不要使用 Markdown 代码块包裹。
正文和标题中禁止出现【编辑推荐】、编辑推荐、推荐理由、★、❖、Markdown 符号。
整篇输出不得出现这些词：读者、帮助、适合、值得、必读。

固定输出结构：

只输出 JSON，字段如下：
{{
  "daily_text": "【今日荐书】\\n\\n主题说明：\\n用 1 句话说明今天这组书共同讨论的主题，不写阅读对象、阅读效果或升华。\\n\\n①《书名》\\n作者：\\n评分：\\n正文：\\n一段 150-250 个中文汉字的书籍介绍。\\n\\n②《书名》\\n作者：\\n评分：\\n正文：\\n一段 150-250 个中文汉字的书籍介绍。\\n\\n③《书名》\\n作者：\\n评分：\\n正文：\\n一段 150-250 个中文汉字的书籍介绍。",
  "books": [
    {{"index": 1, "summary": "第一本书正文，150-250 个中文汉字"}},
    {{"index": 2, "summary": "第二本书正文，150-250 个中文汉字"}},
    {{"index": 3, "summary": "第三本书正文，150-250 个中文汉字"}}
  ]
}}

要求：
- daily_text 必须是最终可展示的中文每日荐书正文，输出到第三本结束即可。
- daily_text 中每本书的“正文”必须和 books 数组里对应 summary 完全一致。
- books 必须严格包含 3 项，并按输入书籍顺序排列。
- 不要在第三本之后再加任何选书或推荐段落。
- 不要写总结段。
- 不要写结尾升华。

正文写法要求：
- 重点写“书讲了什么”，不写阅读对象、阅读效果或人群建议。
- 使用第三人称介绍书籍，不要直接对读者说话。
- 保留书籍事实信息，不使用 AI 式总结。
- 正文中禁止重复完整书名，不要写“《书名》……”，统一使用“本书”“该书”“作者”等指代。
- 可以引用简介里的事实，例如年代、人物、理论、故事设定、主要章节、核心概念、作者背景、经典地位。
- 历史书：写时代背景、关键人物、叙述方法、史料基础和作者分析。
- 小说：写故事设定、主要冲突和文学价值，不要剧透关键结局。
- 科幻：写世界观、核心设定和科幻史地位，不要剧透关键结局。
- 心理学/社科：写核心理论、研究对象和现实案例。
- 个人成长：写主要方法、概念框架、解决的问题。
- 严禁剧透关键情节或结局。
- 每本书正文必须控制在 150-250 个中文汉字，最多 4 句，严格不要超过 250 个中文汉字；少于 150 个汉字时，只能补充书籍事实、内容背景、作者信息或核心概念，不能通过添加感悟、总结、升华句来凑字数；如果初稿超过 250 个中文汉字，必须压缩后再输出。

禁止的内容和句式：
- 不要添加阅读对象小节。
- 不要添加单句概括小节。
- 不要添加末尾选书小节。
- 不要在三本书之间挑选一本作为结论。
- 不要写横向对比、决策建议或推荐排行榜。
- 不要使用“如果你想……”“你会发现……”“你会想到……”等直接对读者说话的句式。
- 不要使用“读完后……”“读到后面……”“读它的时候……”“读完会……”“会开始……”“很容易想到……”等心理描写句式。
- 不要使用“值得一读”“很有启发”“很有回声”“重新理解自己”“重新安排注意力”“这种体验很珍贵”“献给所有……”“值得每个人阅读……”等句式。
- 不要写人群建议、阅读效果预判、心理描写、人生感悟或必要性判断，正文只介绍书的内容和价值。
- 输出前自检：每本书的“正文”都必须是书籍内容介绍，不得转成人群建议或阅读效果预判。

语言风格：
- 更接近出版社简介、优秀书评、克制清楚的微信公众号读书推荐。
- 不要像 AI 读后感、AI 式总结、心理鸡汤、散文或营销文案。
- 不要使用过度文学化、刻意升华的结尾；每本书正文自然收住即可。

书籍信息：
{books_text}
"""
